///////////////////////////////////////////////////////////////////////////////////////////////
//
//  checkPost( form )
//
///////////////////////////////////////////////////////////////////////////////////////////////

function checkPost( form ) {

  if ( (form.Subject.value == '') ||
       (form.Message.value == '') ) {
    alert( 'Sorry, but you missed a field!' );
    return false;
  }

  return true;

}

