package Skinflowers::SubArgs;

###############################################################################################

use strict;
use warnings;

###############################################################################################
##
##  new( [, args] )
##
###############################################################################################

sub new {

  my ( $pkg ) = shift();

  # bless instance
  my $this = bless {}, $pkg;

  # process args
  for ( my $i=0; @_; $i+=2 ) {

    my $name = shift();
    my $value = shift();

    $this->{$name} = $value;

  }

  # return self
  return $this;

}

###############################################################################################
##
##  param( name )
##
###############################################################################################

sub param {

  my ( $this ) = shift();
  my ( $param ) = @_;

  # return value
  return $this->{$param};

}

###############################################################################################
##
##  AUTOLOAD()
##
###############################################################################################

sub AUTOLOAD {

  my ( $this ) = shift();

  # get sub name
  my ( $sub ) = $Skinflowers::SubArgs::AUTOLOAD;

  # remove packages
  $sub =~ s/^.*:://;

  # execute sub
  $this->param( $sub, @_ );

}

###############################################################################################

1;

