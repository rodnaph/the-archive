package Skinflowers::DatabaseResult;

###############################################################################################

use strict;
use warnings;

###############################################################################################
##
##  new( sth )
##
###############################################################################################

sub new {

  my ( $pkg ) = shift();
  my ( $sth ) = @_;

  # bless instance of self
  my $this = bless { sth => $sth }, $pkg;

  # return self
  return $this;

}

###############################################################################################
##
##  getNextRow()
##
###############################################################################################

sub getNextRow {

  my ( $this ) = shift();

  # get next row
  my $rh_row = $this->{sth}->fetchrow_hashref();

  # return the row
  return $rh_row;

}

###############################################################################################
##
##  AUTOLOAD()
##
###############################################################################################

sub AUTOLOAD {

  my ( $this ) = shift();

  # get sub name
  my ( $sub ) = $Skinflowers::DatabaseResult::AUTOLOAD;

  # erase package names
  $sub =~ s/^.*:://;

  # execute subroutine on statement handler
  $this->{sth}->$sub( @_ );

}

###############################################################################################

1;

