var FileTypes = {
 getImage:FileTypes_getImage,
 TXT:'txt.gif',
 PL:'pl.gif',
 FILE:'file.gif',
 HTML:'html.gif',
 CSS:'css.gif',
 JS:'js.gif',
 ASP:'asp.gif',
 INC:'inc.gif'
}

// pre-load images
for(var i in FileTypes){
 var objImage=new Image();
 objImage.src='images/files/'+eval('FileTypes.'+i)
}

///////////////////////////////////////////////////////////////////////////////////////////////
//
//  FileTypes_getImage(type)
//
///////////////////////////////////////////////////////////////////////////////////////////////

function FileTypes_getImage(type){
 type=(type==null)?FileTypes.FILE:type;
 return '<img src="images/files/'+type+'" border="0" alt="" />'
}

