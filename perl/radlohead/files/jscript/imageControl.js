/////////////////////////////////////////////////////////////////////
//
//  swapImage(id,Image)
//
/////////////////////////////////////////////////////////////////////

function swapImage(id,Image) {
  var docObjImage = eval('document.'+id);
  docObjImage.src = Image.src;
}

/////////////////////////////////////////////////////////////////////
//
//  swapMenuInfo(Image)
//
/////////////////////////////////////////////////////////////////////

function swapMenuInfo(Image) {
  swapImage('menu_info',Image);
}

/////////////////////////////////////////////////////////////////////
//
//  swapMenuImage(Image)
//
/////////////////////////////////////////////////////////////////////

function swapMenuImage(Image) {
  swapImage('menu_image',Image);
}

/////////////////////////////////////////////////////////////////////
//
//  resetMenuInfo()
//
/////////////////////////////////////////////////////////////////////

function resetMenuInfo() {
  swapImage('menu_info',blankInfoImg);
}

/////////////////////////////////////////////////////////////////////
//
//  resetMenuImage()
//
/////////////////////////////////////////////////////////////////////

function resetMenuImage() {
  swapImage('menu_image',menuBlankImg);
}

/////////////////////////////////////////////////////////////////////
//
//  resetMenu()
//
/////////////////////////////////////////////////////////////////////

function resetMenu() {
  resetMenuInfo();
  resetMenuImage();
}

/////////////////////////////////////////////////////////////////////
//
//  swapMenu(name)
//
/////////////////////////////////////////////////////////////////////

function swapMenu(name) {

  var infoImg = eval(name+'Img');
  var menuImg = eval('menu'+name+'Img');

  swapMenuInfo(infoImg);
  swapMenuImage(menuImg);

}

