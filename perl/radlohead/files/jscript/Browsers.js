//////////////////////////////////////////////////////////////////
//
//  is_ie() : boolean
//
//////////////////////////////////////////////////////////////////

function is_ie() {
  if ((navigator.appName == 'Microsoft Internet Explorer') && (navigator.userAgent.indexOf('Opera') == -1)) {
    return true;
  } else {
    return false;
  }
}

//////////////////////////////////////////////////////////////////
//
//  is_nn() : boolean
//
//////////////////////////////////////////////////////////////////

function is_nn() {
  if (navigator.appName == 'Netscape') {
    return true;
  } else {
    return false;
  }
}

//////////////////////////////////////////////////////////////////
//
//  is_op() : boolean
//
//////////////////////////////////////////////////////////////////

function is_op() {
  return ((!is_ie()) && (!is_nn()));
}

