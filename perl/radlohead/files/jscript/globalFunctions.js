//  getBase() : base
//
//////////////////////////////////////////////////////////////////

function getBase() {

  if (location.href.indexOf('http://local') != -1) {
    return '//localhost/Archive/2002/';
  } else {
    return '//www.radlohead.com/';
  }

}

//////////////////////////////////////////////////////////////////
//
//  editProfile()
//
//////////////////////////////////////////////////////////////////

function editProfile() {

  var username = escape(prompt('Please enter the username you wish to edit:',''));

  if (username != '') {
    top.location = getBase()+ '04.pl?todo=edit&username=' +username;
  }

}

//////////////////////////////////////////////////////////////////
//
//  viewLetter(objSelect)
//
//////////////////////////////////////////////////////////////////

function viewLetter(objSelect) {

  var from = objSelect.options[objSelect.selectedIndex].value;

  top.location = getBase()+ '04.pl?todo=list&from=' +from;

}

